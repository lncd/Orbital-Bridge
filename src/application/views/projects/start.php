<p>Adding a research project to your Research Dashboard is the easiest way to create a complete research environment for your project, including making the University's Research and Enterprise office aware of what you're working on, adding the project to your Staff Profile, preparing groups and permissions in the CKAN research data repository and more.</p>

<p>You can use Researcher Dashboard to add unfunded projects, or the University's AMS to add funded projects. To get started, choose the type of project you want to add.</p>

